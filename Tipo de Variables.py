# Aquí hay una variable de tipo entero
numero = 147
print(numero) # Esta línea sirve para imprimir la variable número
print(type(numero))

# Redefinimos la variable número, de una entera a una flotante
numero = 147.5
print(numero)
print(type(numero))

complejo = 1 + 2j # Aquí J indica el valor irreal
print (complejo)
print (type(complejo))

palabra = "Hola"
print(palabra)
print (type(palabra))

bandera = True
print (bandera)
print (type(bandera))

vacio = None
print(vacio)
print(type(vacio))



